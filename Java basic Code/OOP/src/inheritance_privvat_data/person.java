
package inheritance_privvat_data;


public class person {

      private  String name;
    private String gander;
    private int age;
    private int moble;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGander() {
        return gander;
    }

    public void setGander(String gander) {
        this.gander = gander;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getMoble() {
        return moble;
    }

    public void setMoble(int moble) {
        this.moble = moble;
    }
  
       
} 
     
    

    

